public class Elanik {
    private String elanik;

    public Elanik(String elanik) {
        this.elanik = elanik;
    }

    public String getElanik() {
        return elanik;
    }

    @Override
    public String toString() {
        return "Elanik{" +
                "elanik='" + elanik + '\'' +
                '}';
    }
}
